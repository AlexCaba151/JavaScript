/*Diferente tipos de Variables en JS*/

//var: Fue el primer tipo que se utilizo pero ya no se recomienda hacerlo
var helloWorld = "Hello, Javascript!"
console.log(helloWorld)

helloWorld =("Hola de nuevo, Javascript")
console.log(helloWorld)

//let: Es el mas comun y el que se recomienda usar actualmente para declarar una variable.

let helloWorld2 = "Hello, Javascript 2"
console.log(helloWorld2)

helloWorld = ("Hola de nuevo, Javascript 2")
console.log(helloWorld2)

//const: Se utiliza para declara variable que no cambiaran su valor, osea que son constantes

const helloWorld3 = "Hello, Javascript 3"
console.log(helloWorld3)